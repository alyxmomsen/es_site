    <p>Мальчик к папе подошёл <br>Надо разобраться! <br>Жить, как ты, я пап хочу <br>Чтоб не напрягаться!</p>
    <p>Тут сынок совет простой <br>Главное жениться <br>И пойми, любовь - отстой  <br>Надо торопиться</p>
    <p>Выбери себе невесту <br>По примеру матери <br>Из богатой, модной знати <br>Этого нам хватит</p>
    <p>Ну а то, что до тебя <br>Сладко жить любила:  <br>И мужчин, и юношей <br>Ртом благодарила,</p>
    <p>Красота фальшивая <br>Ложью, ленью светится <br>Это мелочь, ерунда <br>Попривыкнешь, стерпится</p>
    <p>Дальше надо попотеть <br>Твой отец намаялся <br>У собак учись сынок,  <br>Лихо получается!</p>
    <p>Аккуратно вовремя <br>Ей места отхожие, <br>Ты сынок вылизывай, <br>Тыкаясь в них рожей</p>
    <p>Выгляди, как одуванчик <br>Посредине лета, <br>Страстно, яростно дыши <br>И читай куплеты! </p>
    <p>Вот такая брат наука, <br>Но зато навечно, <br>Жизнь в достатке, сытости <br>Будет обеспечена.</p>

