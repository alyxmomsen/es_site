    <div class="wrapper subject-name"><h2 class="content-politics-title">Сухачев Егор</h2></div>
    <div class="myProfile-container content-about">
    <p>Я веду и пропагандирую абсолютно трезвый образ жизни. Вегетарианец. Оба деда Советские ветераны  Великой
        Отечественной войны. Мать – белорусска по национальности, 1939г.р., закончила торговый техникум, работала по специальности,
        проживает г.Видное, Московская обл. Отец – русский, 1938г.р.,
        высшее образование, по специальности геолог, закончил трудовую деятельность в должности ген.директора «Мегионнефтегазгеология», похоронен в Москве в 2005г.
        У меня 3 родные сестры, одна моя двойняшка. Родился в г.Сургут, Тюменская обл., в 1975г. (38 лет). В возрасте 1 года семья переехала в пос.Горноправдинск, ХМАО, Тюменская обл.
        Там  ходил в старшую и подготовительные группы дедсада и учился в общеобразовательной школе с  1 по 3 класс.
        В 1984г. семья переехала в г.Мегион, Нижневартовский р-он, Тюменская обл. Там  учился в 2-х из существовавших тогда 5 общеобразовательных школ и получил аттестат о среднем образовании в 1992г.
        Летом  1992г. сдал вступительные экзамены выездной приёмной комиссии Тюменского индустриального института (ТЮМГНГУ), проводившиеся в г.Нижневартовск.
        Осенью 1992г. с сестрой переехал в г.Тюмень, став студентом 1-го курса факультета «Разработка и эксплуатация нефтяных и газовых месторождений». Примерно в 1994г.
        был осужден по статье «Хулиганство» (драка на улице, потерпевший с фингалом), получил реальный срок лишений свободы, но был освобождён от отбывания наказания по амнистии.
        Окончил ТЮМГНГУ в 1997г., получил диплом и вернулся в г.Мегион. В 2001г. снова преехал в г.Тюмень, из Тюмени в 2004г. переехал в Москву.
        В 2007г. поступил на юридический факультет МГУ (вечернее). Проучился 3 года из необходимых 3,5. Сдал все сессии, кроме последней.
        Из-за конфликта с самоуправствующими преподавателями недоучился, написал заявление. О самом горе-ректоре МГУ смотрите видео на youtube.com «Садовничий считает 15% от 100».</p>
    <p>С декабря 2011г. участник всех крупных Московских акций протеста. В марте 2012г. был наблюдателем на выборах. С мая по июнь 2012г.
        около месяца прожил на «Оккупай» (сначала на Чистых). Самостоятельно организовывал одиночные акции, неоднократно задерживался, привлекался к суду.</p>
    <p>С  13 лет начал зарабатывать. Сначала во время летних каникул на подсобном хозяйстве, на автотранспортном предприятии, затем грузчиком, разнорабочим, сторожем на автостоянке, занимался предпринимательством.</p>
    <p>Официально женат не был. Детей не имел. Гомосексуальных связей не имел. За пределы России выезжал в недельные туристические и рабочие поездки.
        Имею в собственности а/м  Ниссан 2011г., однокомнатную квартиру (35м) Южный округ Москвы, долю в нежилом помещении (90м) Юго-Восточный округ Москвы.
        В выборах не учавствовал. На государственной службе не состоял, членства в партиях и организациях не имел.</p>
